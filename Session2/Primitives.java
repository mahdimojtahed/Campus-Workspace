public class Primitives {

    public static void main(Strings[] args) {

        // Primitive Data types

        byte myByte = 23;
        // byte wrongByte = 128; // remember that byte range is from 2^7 to 2^7-1
        short myShort = 123;
        int myInt = 1234;
        long myLong = 141231L;
        float myFloat = 12576123f;
        double myDouble = 1235123D;

        char myChar = 'A';
        boolean myBoolean = true;

        System.out.println(myByte);
        System.out.println(myShort);
        System.out.println(myInt);
        System.out.println(myLong);
        System.out.println(myFloat);
        System.out.println(myDouble);
        System.out.println(myChar);
        System.out.println(myBoolean);


        // Hands-On Exercise :
        byte age = 26;
        float height = 180.1F;
        char bloodType = 'B';
        boolean male = true;
        int telephone = 214124;
        long mobile = 9199999999L;
        short level = 2;
        double bankAccount = 1421653545313D;

    }

}
